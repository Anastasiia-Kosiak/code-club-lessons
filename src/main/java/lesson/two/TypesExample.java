package lesson.two;

public class TypesExample {
    public static void main(String[] args) {
        boolean bool = true;
        byte b = 67; // 8 бітів => -2^7 - 2^7 +1 = -256 - 255
        System.out.println(b);
        short sh = 567;
        System.out.println(sh);
        int i = 56778;
        System.out.println(i);
        int iNegative = -56;
        System.out.println(iNegative);
        long l = 560000000;
        float f = 56.78f; //floating point - плаваюча крапка - із десяткою частиною
        System.out.println(f);
        double d = 567.4555;
        System.out.println(d);

    }
}
